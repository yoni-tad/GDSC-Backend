/*
    What is middleware?
        Middlewares are functions that have access to the request object (req), the response object (res) 
        and the next middleware function in the application request-response cycle.
    Middleware functions are are executed sequentially in the order they are defined.
    The middleware function can pass control to the next middleware function using the next() function.
    Benefits:
        Modularity
        Reusability
        Separation of concerns
    


*/